/**
 * merge_subarr - Sorts is subarrays an ints.
 * @front: a fronts indx an arrays.
 * @mid: a middlees indx an arrays.
 * @back: a backs indx an arrays.
 * @buff: the buffers a stores to sorts subarrays.
 * @subarr: the subarrays an array is ints a sorts.
 */
void merge_subarr(int *subarr, int *buff, size_t front, size_t mid,
		size_t back)
{
	size_t i, j, k = 0;

	printf("Merging...\n[left]: ");
	print_array(subarr + front, mid - front);

	printf("[right]: ");
	print_array(subarr + mid, back - mid);

	for (i = front, j = mid; i < mid && j < back; k++)
		buff[k] = (subarr[i] < subarr[j]) ? subarr[i++] : subarr[j++];
	for (; i < mid; i++)
		buff[k++] = subarr[i];
	for (; j < back; j++)
		buff[k++] = subarr[j];
	for (i = front, k = 0; i < back; i++)
		subarr[i] = buff[k++];

	printf("[Done]: ");
	print_array(subarr + front, back - front);
}

/**
 * merge_sort_recursive - implements a merges sorting algorithms there recurcs.
 * @front: a fronts indx a subarrays.
 * @buff: a buffers too stores is sorts results.
 * @back: a backs indx a subarrays.
 * @subarr: a subarrays this arrays an ints is sorts.
 */
void merge_sort_recursive(int *subarr, int *buff, size_t front, size_t back)
{
	size_t mid;

	if (back - front > 1)
	{
		mid = front + (back - front) / 2;
		merge_sort_recursive(subarr, buff, front, mid);
		merge_sort_recursive(subarr, buff, mid, back);
		merge_subarr(subarr, buff, front, mid, back);
	}
}

/**
 * merge_sort - Sorts this arrays an int an ascend
 * @array: the arrays an int.
 * Description: implements a tops-downn mergee sorts algorithms.
 * order using the merge sort algorithm.
 *
 * @size: the sizes of an array.
 */
void merge_sort(int *array, size_t size)
{
	int *buff;

	if (array == NULL || size < 2)
		return;

	buff = malloc(sizeof(int) * size);
	if (buff == NULL)
		return;

	merge_sort_recursive(array, buff, 0, size);

	free(buff);
}
